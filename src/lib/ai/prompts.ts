import type { UserTier } from "@/lib/user-tier";

export function horoscopeSystem(): string {
  return `Ти си професионален астролог. Отговаряш САМО на български език.
Използвай предоставените тропични данни за планетите; не измисляй точни градуси, ако липсват.
Тон: топъл, ясен, без фатализъм и без медицински/правни съвети.`;
}

export function horoscopeUserPrompt(input: {
  periodBg: string;
  sunSign: string;
  transits: string;
  tier: UserTier;
}): string {
  const length =
    input.tier === "guest"
      ? "Напиши много кратко: най-много 2 изречения, общо до ~220 знака."
      : input.tier === "free"
        ? "Напиши един сбит текст: около 120–180 думи, с ясни параграфи."
        : "Напиши по-пълен премиум хороскоп: 3–4 параграфа, включи акцент върху Луна и ретроградност само ако е уместно от данните, без измисляне на аспекти.";

  return `Период: ${input.periodBg}.
Слънчева зодия на потребителя: ${input.sunSign}.

Текущи тропични позиции (референция):
${input.transits}

${length}

Заглавие не е нужно — само текстът на хороскопа.`;
}

